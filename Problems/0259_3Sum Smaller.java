class Solution {
    public int threeSumSmaller(int[] nums, int target) {
        int n = nums.length;
        int count = 0;
        Arrays.sort(nums);
        for (int i = 0; i < n - 2; i++) {
            int j = i + 1;
            for (; j < n - 1; j++) {
                int k = j + 1;
                for (; k < n; k++) {
                    if (nums[i] + nums[j] + nums[k] < target) {
                        count++;
                    } else {
                        break;
                    }
                }
                if (k <= j + 1) break;
            }
            if (j <= i + 1) break;
        }
        return count;
    }
}

